CREATE TABLE IF NOT EXISTS comptesmails (
    IDComptesMails INT AUTO_INCREMENT PRIMARY KEY,
    AdresseMail VARCHAR(255) NOT NULL,
    MotDePasse VARCHAR(255) NOT NULL,
    LibelleMail VARCHAR(255),
    ServeurPOP VARCHAR(255),
    PortPOP VARCHAR(10) DEFAULT '110',
    ServeurSMTP VARCHAR(255),
    PortSMTP VARCHAR(10) DEFAULT '587',
    ServeurIMAPEntrant VARCHAR(255),
    PortIMAPEntrant VARCHAR(10) DEFAULT '143',
    ServeurIMAPSortant VARCHAR(255),
    PortIMAPSortant VARCHAR(10) DEFAULT '587',
    structur_id INT,
    CreatedAt TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    UpdatedAt TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    INDEX (structur_id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;
